              <p>Tecamachalco, Pue. 31 de agosto del 2012 En gira de trabajo por Tecamachalco, el Gobernador del Estado de Puebla, Rafael Moreno Valle, acompañado del Presidente Municipal, Rubén Jesús Balcázar Juárez, entregó 99 mototractores, equivalentes a una inversión de 11 millones 384 mil pesos, en beneficio de 297 familias campesinas de Tecamachalco, Xochitlán, Palmar del Bravo y Tochtepec. Al evento acudieron los diputados federal y local Fernando Morales e Inés Saturnino López respectivamente, así como el encargado de Despacho de la Secretaría de Desarrollo Rural, Rodrigo Riestra Piña. </p>
