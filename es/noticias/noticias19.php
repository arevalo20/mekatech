              <h3>Capacitan a productores sobre uso de motocultores en Cuyoaco.</h3>
              <p>Cuyoaco, Pue. 25 de mayo del 2012. Los cursos son esenciales para garantizar el uso adecuado de este tipo de tecnología agrícola Cuyoaco, Pue.- Para evitar el uso incorrecto de los mototractores, los productores de Cuyoaco tomaron un curso de capacitación y posteriormente recibieron el equipo que les ayudará en la siembra y cosecha de sus tierras. </p>
              <p>El técnico Asesor, Eleazar Pérez Carlos, dijo que en este curso se les enseñó el uso correcto del equipo, ya que son pequeños productores que no rebasen las 8 ó 10 hectáreas y ellos mismo pueden manejar los mototractores, pues es una forma viable para que siembren. "La capacitación consiste en que el productor conozca las partes del motocultor, sepa cuestiones básicas de mantenimiento y aprenda a operarlo, porque son equipos que funcionan con diesel y tienen múltiples funciones, podemos hacer labores previas de preparación, como son barbechos, rastra y también las otras funciones como: fumigar, regar, generar electricidad, deshojar y desgranar."</p>
              <p>Dijo que la capacitación y orientación es esencial para garantizar el uso adecuado de este tipo de tecnología agrícola, para ser sustituidos por los animales que comúnmente utilizan los agricultores para las actividades de arado en las tierras de labor. Finalmente indicó que sirve como vehículo de transite con un remolque adherido, así como para carga, transporte y limpia; su uso está recomendando en granjas, ranchos, hortalizas frutales, e invernaderos.</p>
