              <p>Ciudad Serdán, Pue. 6 de enero del 2012. Al entregar apoyos por más de 38 millones de pesos a productores del campo, el Gobernador Rafael Moreno Valle advirtió que no permitirá que el proceso electoral interrumpa la transformación de Puebla. Y precisó: "la democracia es un asunto que deben resolver los ciudadanos y en los temas de gobierno nos toca trabajar para todos". Moreno Valle entregó 344 mototractores -los primeros del año- con una inversión superior a 34 millones de pesos.</p>
