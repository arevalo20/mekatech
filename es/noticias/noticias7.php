              <h3>Mecanización del Campo en Guerrero para el Pequeño Productor</h3>
              <p>Ayutla de los libres, Gue., 11 julio 2011.- En las comunidades de tres Cruces y Concordia pertenecientes al municipio de Ayutla de los Libres Guerrero; se llevaron a cabo las primeras demostraciones de los Mototractores MekaTech (motocultores), ante más de 800 personas respectivamente. Durante la presentación, los productores de la montaña de Guerrero pudieron observar como esta tecnología de Mototractores (motocultores) de 2 ruedas, podrán ayudarles a hacer más productivos en sus cultivos, principalmente en el cultivo de maíz, utilizando la desgranadora-deshojadora, que esta nueva tecnología ofrece.</p>
